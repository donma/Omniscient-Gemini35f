const currentFilters = {
    search: '',
    industry: '全部',
    colorTone: '全部',
    colorGroup: '全部',
    features: [],
    layout: '全部'
};

document.addEventListener('DOMContentLoaded', () => {
    const isDetailPage = window.location.pathname.includes('template-detail.html');
    init(isDetailPage);

    // Mobile Sidebar toggler
    const sidebarToggle = document.getElementById('sidebarToggle');
    const sidebar = document.getElementById('gallerySidebar');
    if (sidebarToggle && sidebar) {
        sidebarToggle.addEventListener('click', () => {
            sidebar.classList.toggle('show');
        });
        document.addEventListener('click', (e) => {
            if (!sidebar.contains(e.target) && !sidebarToggle.contains(e.target) && sidebar.classList.contains('show')) {
                sidebar.classList.remove('show');
            }
        });
    }
});

function init(isDetailPage) {
    if (isDetailPage) {
        initDetailPage();
    } else {
        initGalleryPage();
    }
}

function initGalleryPage() {
    renderTemplates();

    const searchInput = document.getElementById('searchInput');
    if (searchInput) {
        searchInput.addEventListener('input', (e) => {
            currentFilters.search = e.target.value.toLowerCase().trim();
            renderTemplates();
        });
    }

    const industryInputs = document.querySelectorAll('input[name="filter-industry"]');
    industryInputs.forEach(input => {
        input.addEventListener('change', (e) => {
            currentFilters.industry = e.target.value;
            renderTemplates();
        });
    });

    const colorToneInputs = document.querySelectorAll('input[name="filter-color-tone"]');
    colorToneInputs.forEach(input => {
        input.addEventListener('change', (e) => {
            currentFilters.colorTone = e.target.value;
            renderTemplates();
        });
    });

    const colorGroupInputs = document.querySelectorAll('input[name="filter-color-group"]');
    colorGroupInputs.forEach(input => {
        input.addEventListener('change', (e) => {
            currentFilters.colorGroup = e.target.value;
            renderTemplates();
        });
    });

    const featureInputs = document.querySelectorAll('input[name="filter-feature"]');
    featureInputs.forEach(input => {
        input.addEventListener('change', () => {
            currentFilters.features = Array.from(featureInputs)
                .filter(i => i.checked)
                .map(i => i.value);
            renderTemplates();
        });
    });

    const layoutInputs = document.querySelectorAll('input[name="filter-layout"]');
    layoutInputs.forEach(input => {
        input.addEventListener('change', (e) => {
            currentFilters.layout = e.target.value;
            renderTemplates();
        });
    });
}

function renderTemplates() {
    const grid = document.getElementById('templatesGrid');
    if (!grid) return;

    const filtered = templatesData.filter(t => {
        if (currentFilters.search) {
            const matchesSearch = t.name.toLowerCase().includes(currentFilters.search) ||
                (t.industryCategory && t.industryCategory.toLowerCase().includes(currentFilters.search)) ||
                t.industry.toLowerCase().includes(currentFilters.search) ||
                t.description.toLowerCase().includes(currentFilters.search) ||
                (t.tags && t.tags.some(tag => tag.toLowerCase().includes(currentFilters.search)));
            if (!matchesSearch) return false;
        }

        if (currentFilters.industry !== '全部' && t.industryCategory !== currentFilters.industry) {
            return false;
        }

        if (currentFilters.colorTone !== '全部' && t.colorTone !== currentFilters.colorTone) {
            return false;
        }

        if (currentFilters.colorGroup !== '全部' && t.colorTheme !== currentFilters.colorGroup) {
            return false;
        }

        if (currentFilters.features.length > 0) {
            const hasAllFeatures = currentFilters.features.every(feat => t.tags && t.tags.includes(feat));
            if (!hasAllFeatures) return false;
        }

        if (currentFilters.layout !== '全部' && t.layoutFamily !== currentFilters.layout) {
            return false;
        }

        return true;
    });

    if (filtered.length === 0) {
        grid.innerHTML = `
            <div class="col-12 text-center py-5">
                <i class="bi bi-search fs-1 text-muted mb-3 d-block"></i>
                <h4 class="text-white fw-bold">找不到符合條件的網頁樣板</h4>
                <p class="text-muted">請嘗試調整您的篩選或搜尋條件。</p>
            </div>
        `;
        return;
    }

    grid.innerHTML = filtered.map(t => {
        const tagsHtml = (t.tags || []).slice(0, 6).map(tag => `<span class="tag-badge">${tag}</span>`).join('');
        
        let zipBtnHtml = `<span class="feature-indicator active"><a href="${t.zipUrl}" class="text-decoration-none text-accent" download><i class="bi bi-file-zip"></i> 下載 ZIP</a></span>`;
        if (t.hasZip === false) {
            zipBtnHtml = `<span class="feature-indicator text-muted"><i class="bi bi-file-zip"></i> ZIP 待打包</span>`;
        }

        let extraMeta = '';
        if (t.tags && t.tags.includes("圖片豐富")) {
            extraMeta = `<span class="meta-badge" style="background:#4a2b99;">圖片豐富</span>`;
        }

        return `
            <div class="col-xl-4 col-md-6 mb-4">
                <div class="template-card">
                    <div class="template-img-wrapper">
                        <img src="${t.previewImage}" alt="${t.name} 首頁縮圖" class="template-img" onerror="this.src='templates/${t.id}-${t.slug}/assets/img/preview.jpg'">
                        <div class="template-overlay">
                            <a href="${t.demoUrl}" target="_blank" class="btn btn-premium rounded-pill px-3 py-2"><i class="bi bi-eye-fill me-1"></i>預覽網站</a>
                            <a href="template-detail.html?id=${t.id}" class="btn btn-glass rounded-pill px-3 py-2"><i class="bi bi-info-circle-fill me-1"></i>查看詳情</a>
                        </div>
                    </div>
                    <div class="template-body">
                        <h3 class="template-name">${t.name}</h3>
                        <div class="template-meta">
                            <span class="meta-badge">${t.industryCategory || t.industry}</span>
                            <span class="meta-badge bg-tone">${t.colorTheme}</span>
                            <span class="meta-badge" style="background:#2b5c99; color:#fff;">${t.layoutFamily}</span>
                            ${extraMeta}
                        </div>
                        <p class="text-card-desc small mb-3">${t.description}</p>
                        <div class="template-tags">
                            ${tagsHtml}
                        </div>
                        <div class="template-features">
                            <span class="feature-indicator active"><i class="bi bi-file-earmark-code"></i> ${t.pageCount || 12} 頁面</span>
                            ${zipBtnHtml}
                            <span class="feature-indicator active"><a href="${t.promptUrl}" target="_blank" class="text-decoration-none text-accent"><i class="bi bi-terminal"></i> Prompt</a></span>
                        </div>
                    </div>
                </div>
            </div>
        `;
    }).join('');
}

function initDetailPage() {
    const params = new URLSearchParams(window.location.search);
    const templateId = params.get('id');

    if (!templateId) {
        window.location.href = 'index.html';
        return;
    }

    const t = templatesData.find(item => item.id === templateId);
    if (!t) {
        window.location.href = 'index.html';
        return;
    }

    document.title = `${t.name} 樣板詳情 | 全職設計視角 | AI 網頁樣板庫`;
    
    const detailName = document.getElementById('detailName');
    const detailDesc = document.getElementById('detailDesc');
    const detailIndustry = document.getElementById('detailIndustry');
    const detailTheme = document.getElementById('detailTheme');
    const detailDemoBtn = document.getElementById('detailDemoBtn');
    const detailZipBtn = document.getElementById('detailZipBtn');
    const detailPromptBtn = document.getElementById('detailPromptBtn');
    const detailImg = document.getElementById('detailImg');
    const detailTags = document.getElementById('detailTags');

    if (detailName) detailName.textContent = t.name;
    if (detailDesc) detailDesc.textContent = t.description;
    if (detailIndustry) detailIndustry.textContent = t.industryCategory || t.industry;
    if (detailTheme) detailTheme.textContent = t.colorTheme;
    const detailLayoutFamily = document.getElementById('detailLayoutFamily');
    if (detailLayoutFamily) detailLayoutFamily.textContent = t.layoutFamily;
    
    if (detailDemoBtn) detailDemoBtn.href = t.demoUrl;
    
    if (detailZipBtn) {
        if (t.hasZip === false) {
            detailZipBtn.textContent = 'ZIP 待打包';
            detailZipBtn.classList.add('disabled');
            detailZipBtn.style.pointerEvents = 'none';
        } else {
            detailZipBtn.href = t.zipUrl;
            detailZipBtn.setAttribute('download', '');
        }
    }
    if (detailPromptBtn) detailPromptBtn.href = t.promptUrl;
    
    if (detailImg) {
        detailImg.src = t.previewImage;
        detailImg.onerror = function() {
            this.src = `templates/${t.id}-${t.slug}/assets/img/preview.jpg`;
        };
    }

    if (detailTags) {
        detailTags.innerHTML = (t.tags || []).map(tag => `<span class="tag-badge fs-6 me-2 mb-2 d-inline-block">${tag}</span>`).join('');
    }
}
